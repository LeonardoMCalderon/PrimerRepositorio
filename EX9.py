"""PROMEDIO DE UN ALUMNO"""

Español = int(input("Insertel el valor de la asignatura de español"))
Matematicas= int(input("Insertel el valor de la asignatura de Matematicas"))
Economia= int(input("Insertel el valor de la asignatura de Economia"))
Ingles= int(input("Insertel el valor de la asignatura de Ingles"))
Programacion= int(input("Insertel el valor de la asignatura de Programacion"))

Promedio = (Español + Matematicas + Economia + Ingles + Programacion)/5

print("El promedio del alumno es de", Promedio)